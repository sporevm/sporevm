pub fn df(allocator: std.mem.Allocator, io: Io, options: DfOptions) !RootfsSystemSummary {
    var arena_state = std.heap.ArenaAllocator.init(allocator);
    defer arena_state.deinit();

    var summary = try summarizeRootfsCache(arena_state.allocator(), io, options.cache_root);
    summary.cache_root = try allocator.dupe(u8, options.cache_root);
    return summary;
}

pub fn deinitRootfsSystemSummary(allocator: std.mem.Allocator, summary: RootfsSystemSummary) void {
    allocator.free(summary.cache_root);
}

pub fn prune(
    allocator: std.mem.Allocator,
    io: Io,
    options: PruneOptions,
    now_ns: i96,
) !RootfsPruneResult {
    const plan_options = try normalizePruneOptions(options);

    var arena_state = std.heap.ArenaAllocator.init(allocator);
    defer arena_state.deinit();
    const arena = arena_state.allocator();

    var result = try pruneRootfsCache(arena, io, options.cache_root, plan_options, now_ns);
    if (!options.rootfs_only and plan_options.older_than_ns != null) {
        if (options.runtime_root) |runtime_root| {
            result.runtime_forks = try pruneRuntimeForkBatches(arena, io, runtime_root, plan_options, now_ns);
        }
    }
    return ownRootfsPruneResult(allocator, result);
}

pub fn gc(
    allocator: std.mem.Allocator,
    io: Io,
    options: GcOptions,
) !RootfsGcResult {
    var arena_state = std.heap.ArenaAllocator.init(allocator);
    defer arena_state.deinit();
    const arena = arena_state.allocator();

    const result = try gcRootfsCache(arena, io, options);
    return ownRootfsGcResult(allocator, result);
}

pub fn deinitRootfsPruneResult(allocator: std.mem.Allocator, result: RootfsPruneResult) void {
    allocator.free(result.cache_root);
    for (result.entries) |entry| {
        allocator.free(entry.path);
        if (entry.metadata_path) |path| allocator.free(path);
    }
    allocator.free(result.entries);
    if (result.runtime_forks) |runtime_forks| {
        deinitRuntimeForkPruneResult(allocator, runtime_forks);
    }
}

pub fn deinitRootfsGcResult(allocator: std.mem.Allocator, result: RootfsGcResult) void {
    allocator.free(result.cache_root);
    for (result.entries) |entry| {
        allocator.free(entry.path);
    }
    allocator.free(result.entries);
}

pub fn run(
    init: std.process.Init,
    args: []const []const u8,
    stdout: *Io.Writer,
    stderr: *Io.Writer,
    mode: machine_output.Mode,
) !void {
    if (args.len == 0 or wantsHelp(args[0])) {
        if (mode == .json) {
            exitWithCliError(
                init.arena.allocator(),
                stderr,
                mode,
                machine_output.usageMissingArgument("usage: spore system <df|prune>", "system"),
                "usage: spore system <df|prune>",
            );
        }
        try stdout.writeAll(usage);
        return;
    }

    if (std.mem.eql(u8, args[0], "df")) {
        try dfCli(init, args[1..], stdout, stderr, mode);
        return;
    }
    if (std.mem.eql(u8, args[0], "prune")) {
        try pruneCli(init, args[1..], stdout, stderr, mode);
        return;
    }

    const message = allocMessage(init.arena.allocator(), "unknown system command: {s}", .{args[0]});
    exitWithCliError(init.arena.allocator(), stderr, mode, machine_output.usageInvalidArgument(message, "system"), messageWithUsage(init.arena.allocator(), message));
}

pub fn cacheRun(
    init: std.process.Init,
    args: []const []const u8,
    stdout: *Io.Writer,
    stderr: *Io.Writer,
    mode: machine_output.Mode,
) !void {
    if (args.len == 0 or wantsHelp(args[0])) {
        if (mode == .json and args.len == 0) {
            exitWithCliError(
                init.arena.allocator(),
                stderr,
                mode,
                machine_output.usageMissingArgument("usage: spore cache <gc>", "cache"),
                "usage: spore cache <gc>",
            );
        }
        try stdout.writeAll(cache_usage);
        return;
    }
    if (std.mem.eql(u8, args[0], "gc")) {
        try cacheGcCli(init, args[1..], stdout, stderr, mode);
        return;
    }
    const message = allocMessage(init.arena.allocator(), "unknown cache command: {s}", .{args[0]});
    exitWithCliError(init.arena.allocator(), stderr, mode, machine_output.usageInvalidArgument(message, "cache"), messageWithCacheUsage(init.arena.allocator(), message));
}

fn dfCli(init: std.process.Init, args: []const []const u8, stdout: *Io.Writer, stderr: *Io.Writer, mode: machine_output.Mode) !void {
    for (args) |arg| {
        if (wantsHelp(arg)) {
            if (mode == .json) {
                exitWithCliError(
                    init.arena.allocator(),
                    stderr,
                    mode,
                    machine_output.usageInvalidArgument("spore --json system df does not support help output", "system df"),
                    "spore --json system df does not support help output",
                );
            }
            try stdout.writeAll(usage);
            return;
        } else if (std.mem.eql(u8, arg, "--json")) {
            exitLocalJsonUnsupported(init.arena.allocator(), stderr, mode, "system df");
        } else if (!std.mem.eql(u8, arg, "--rootfs")) {
            exitUnknownArgument(init.arena.allocator(), stderr, mode, "system df", arg);
        }
    }

    const allocator = init.arena.allocator();
    const cache_root = local_paths.rootfsCacheRootPath(allocator, init.environ_map) catch |err| switch (err) {
        error.MissingHome => {
            exitWithCliError(
                allocator,
                stderr,
                mode,
                machine_output.CliError.init(.cache_unavailable, "cannot resolve rootfs cache directory; set SPOREVM_ROOTFS_CACHE_DIR or HOME", "MissingHome"),
                "spore system: cannot resolve rootfs cache directory; set SPOREVM_ROOTFS_CACHE_DIR or HOME",
            );
        },
        else => |e| return e,
    };
    const summary = try df(allocator, init.io, .{ .cache_root = cache_root });
    if (mode == .json) {
        try machine_output.writeJson(allocator, stdout, summary);
    } else {
        try writeRootfsSummary(stdout, summary);
    }
}

fn pruneCli(init: std.process.Init, args: []const []const u8, stdout: *Io.Writer, stderr: *Io.Writer, mode: machine_output.Mode) !void {
    var opts = PruneOptions{ .cache_root = "" };

    var i: usize = 0;
    while (i < args.len) : (i += 1) {
        const arg = args[i];
        if (wantsHelp(arg)) {
            if (mode == .json) {
                exitWithCliError(
                    init.arena.allocator(),
                    stderr,
                    mode,
                    machine_output.usageInvalidArgument("spore --json system prune does not support help output", "system prune"),
                    "spore --json system prune does not support help output",
                );
            }
            try stdout.writeAll(usage);
            return;
        } else if (std.mem.eql(u8, arg, "--json")) {
            exitLocalJsonUnsupported(init.arena.allocator(), stderr, mode, "system prune");
        } else if (std.mem.eql(u8, arg, "--rootfs")) {
            opts.rootfs_only = true;
        } else if (std.mem.eql(u8, arg, "--dry-run")) {
            opts.dry_run = true;
        } else if (std.mem.eql(u8, arg, "--force")) {
            opts.dry_run = false;
        } else if (std.mem.eql(u8, arg, "--include-digest-artifacts")) {
            opts.include_digest_artifacts = true;
        } else if (std.mem.eql(u8, arg, "--include-rootfs-chunks")) {
            opts.include_rootfs_chunks = true;
        } else if (std.mem.eql(u8, arg, "--older-than")) {
            i += 1;
            if (i >= args.len) exitMissingValue(init.arena.allocator(), stderr, mode, "system prune", "--older-than");
            const seconds = parseDurationSeconds(args[i]) catch {
                exitInvalidValue(init.arena.allocator(), stderr, mode, "system prune", "--older-than", args[i]);
            };
            opts.older_than_seconds = seconds;
        } else if (std.mem.eql(u8, arg, "--max-bytes")) {
            i += 1;
            if (i >= args.len) exitMissingValue(init.arena.allocator(), stderr, mode, "system prune", "--max-bytes");
            opts.max_bytes = parseByteSize(args[i]) catch {
                exitInvalidValue(init.arena.allocator(), stderr, mode, "system prune", "--max-bytes", args[i]);
            };
        } else {
            exitUnknownArgument(init.arena.allocator(), stderr, mode, "system prune", arg);
        }
    }

    if (opts.older_than_seconds == null and opts.max_bytes == null and (opts.include_digest_artifacts or opts.include_rootfs_chunks)) {
        const message = "spore system prune: set --older-than or --max-bytes when using --include-digest-artifacts or --include-rootfs-chunks";
        exitWithCliError(init.arena.allocator(), stderr, mode, machine_output.usageMissingArgument(message, "system prune"), messageWithUsage(init.arena.allocator(), message));
    }

    const allocator = init.arena.allocator();
    const cache_root = local_paths.rootfsCacheRootPath(allocator, init.environ_map) catch |err| switch (err) {
        error.MissingHome => {
            exitWithCliError(
                allocator,
                stderr,
                mode,
                machine_output.CliError.init(.cache_unavailable, "cannot resolve rootfs cache directory; set SPOREVM_ROOTFS_CACHE_DIR or HOME", "MissingHome"),
                "spore system: cannot resolve rootfs cache directory; set SPOREVM_ROOTFS_CACHE_DIR or HOME",
            );
        },
        else => |e| return e,
    };
    opts.cache_root = cache_root;
    const now = Io.Clock.real.now(init.io).nanoseconds;
    if (!opts.rootfs_only and opts.older_than_seconds != null) {
        const runtime_root = local_paths.runtimeRootPath(allocator, init.environ_map) catch null;
        if (runtime_root) |root| opts.runtime_root = root;
    }
    const result = try prune(allocator, init.io, opts, now);
    if (mode == .json) {
        try machine_output.writeJson(allocator, stdout, result);
    } else {
        try writeRootfsPruneResult(stdout, result);
    }
}

fn cacheGcCli(init: std.process.Init, args: []const []const u8, stdout: *Io.Writer, stderr: *Io.Writer, mode: machine_output.Mode) !void {
    var dry_run = true;

    for (args) |arg| {
        if (wantsHelp(arg)) {
            if (mode == .json) {
                exitWithCliError(
                    init.arena.allocator(),
                    stderr,
                    mode,
                    machine_output.usageInvalidArgument("spore --json cache gc does not support help output", "cache gc"),
                    "spore --json cache gc does not support help output",
                );
            }
            try stdout.writeAll(cache_usage);
            return;
        } else if (std.mem.eql(u8, arg, "--json")) {
            exitLocalJsonUnsupported(init.arena.allocator(), stderr, mode, "cache gc");
        } else if (std.mem.eql(u8, arg, "--rootfs")) {
            // The rootfs CAS is the only cache scope in U1.
        } else if (std.mem.eql(u8, arg, "--dry-run")) {
            dry_run = true;
        } else if (std.mem.eql(u8, arg, "--force")) {
            dry_run = false;
        } else {
            exitUnknownArgument(init.arena.allocator(), stderr, mode, "cache gc", arg);
        }
    }

    const allocator = init.arena.allocator();
    const cache_root = local_paths.rootfsCacheRootPath(allocator, init.environ_map) catch |err| switch (err) {
        error.MissingHome => {
            exitWithCliError(
                allocator,
                stderr,
                mode,
                machine_output.CliError.init(.cache_unavailable, "cannot resolve rootfs cache directory; set SPOREVM_ROOTFS_CACHE_DIR or HOME", "MissingHome"),
                "spore cache gc: cannot resolve rootfs cache directory; set SPOREVM_ROOTFS_CACHE_DIR or HOME",
            );
        },
        else => |e| return e,
    };
    const runtime_root = local_paths.runtimeRootPath(allocator, init.environ_map) catch null;
    const result = try gc(allocator, init.io, .{
        .cache_root = cache_root,
        .runtime_root = runtime_root,
        .dry_run = dry_run,
    });
    if (mode == .json) {
        try machine_output.writeJson(allocator, stdout, result);
    } else {
        try writeRootfsGcResult(stdout, result);
    }
}

