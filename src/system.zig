fn markAllGcCasEntriesRooted(
    allocator: std.mem.Allocator,
    io: Io,
    cache_root: []const u8,
    sub_path: []const u8,
    suffix: []const u8,
    rooted: *std.StringHashMap(void),
) !void {
    const dir_path = try std.fs.path.join(allocator, &.{ cache_root, sub_path });
    var dir = openDirPath(io, dir_path, .{ .iterate = true }) catch |err| switch (err) {
        error.FileNotFound => return,
        else => |e| return e,
    };
    defer dir.close(io);

    var it = dir.iterate();
    while (try it.next(io)) |entry| {
        if (entry.kind != .file or !std.mem.endsWith(u8, entry.name, suffix)) continue;
        const digest = digestFromCacheEntryName(allocator, entry.name, suffix) orelse continue;
        try rooted.put(digest, {});
    }
}

fn collectGcCasEntries(
    allocator: std.mem.Allocator,
    io: Io,
    cache_root: []const u8,
    sub_path: []const u8,
    suffix: []const u8,
    kind: []const u8,
    rooted: *std.StringHashMap(void),
    stamp_invalidation: GcStampInvalidation,
    dry_run: bool,
    entries: *std.array_list.Managed(RootfsGcEntry),
    candidate_bytes: *u64,
    deleted_count: *usize,
    deleted_bytes: *u64,
) !void {
    const dir_path = try std.fs.path.join(allocator, &.{ cache_root, sub_path });
    var dir = openDirPath(io, dir_path, .{ .iterate = true }) catch |err| switch (err) {
        error.FileNotFound => return,
        else => |e| return e,
    };
    defer dir.close(io);

    var it = dir.iterate();
    while (try it.next(io)) |entry| {
        if (entry.kind != .file or !std.mem.endsWith(u8, entry.name, suffix)) continue;
        const digest = digestFromCacheEntryName(allocator, entry.name, suffix) orelse continue;
        if (rooted.contains(digest)) continue;
        const stat = try dir.statFile(io, entry.name, .{ .follow_symlinks = false });
        if (stat.kind != .file) continue;
        const path = try std.fs.path.join(allocator, &.{ dir_path, entry.name });
        try entries.append(.{
            .kind = kind,
            .path = path,
            .bytes = stat.size,
        });
        candidate_bytes.* += stat.size;
        if (!dry_run) {
            switch (stamp_invalidation) {
                .none => {},
                .index => try rootfs_cas.removeStorageCompleteStamp(io, allocator, cache_root, digest),
            }
            try Io.Dir.cwd().deleteFile(io, path);
            deleted_count.* += 1;
            deleted_bytes.* += stat.size;
        }
    }
}

const GcStampInvalidation = enum {
    none,
    index,
};

fn digestFromCacheEntryName(allocator: std.mem.Allocator, name: []const u8, suffix: []const u8) ?[]const u8 {
    if (!std.mem.endsWith(u8, name, suffix)) return null;
    const hex = name[0 .. name.len - suffix.len];
    if (hex.len != chunk.ChunkId.hex_len) return null;
    _ = chunk.ChunkId.fromHex(hex) catch return null;
    return std.fmt.allocPrint(allocator, "{s}{s}", .{ spore.rootfs_digest_prefix, hex }) catch return null;
}

fn isLowerHexDigest(value: []const u8) bool {
    if (value.len != chunk.ChunkId.hex_len) return false;
    for (value) |byte| {
        if ((byte >= '0' and byte <= '9') or (byte >= 'a' and byte <= 'f')) continue;
        return false;
    }
    return true;
}

fn pruneRuntimeForkBatches(
    allocator: std.mem.Allocator,
    io: Io,
    runtime_root: []const u8,
    opts: PrunePlanOptions,
    now_ns: i96,
) !RuntimeForkPruneResult {
    const plan = try collectRuntimeForkEntries(allocator, io, runtime_root);
    std.mem.sort(RuntimeForkPlanEntry, plan, {}, lessRuntimeForkPlanEntry);

    if (opts.older_than_ns) |age_ns| {
        const cutoff = now_ns - age_ns;
        for (plan) |*entry| {
            if (entry.mtime_ns < cutoff) entry.selected = true;
        }
    }

    var result_entries = std.array_list.Managed(RuntimeForkPruneEntry).init(allocator);
    var candidate_bytes: u64 = 0;
    var deleted_count: usize = 0;
    var deleted_bytes: u64 = 0;

    for (plan) |entry| {
        if (!entry.selected) continue;
        candidate_bytes += entry.bytes;
        try result_entries.append(.{
            .path = entry.path,
            .bytes = entry.bytes,
            .mtime_unix = @intCast(@divFloor(entry.mtime_ns, std.time.ns_per_s)),
        });
        if (!opts.dry_run) {
            try Io.Dir.cwd().deleteTree(io, entry.path);
            deleted_count += 1;
            deleted_bytes += entry.bytes;
        }
    }

    const entries = try result_entries.toOwnedSlice();
    return .{
        .runtime_root = runtime_root,
        .dry_run = opts.dry_run,
        .older_than_seconds = opts.older_than_seconds,
        .candidate_count = entries.len,
        .candidate_bytes = candidate_bytes,
        .deleted_count = deleted_count,
        .deleted_bytes = deleted_bytes,
        .entries = entries,
    };
}

fn collectRuntimeForkEntries(
    allocator: std.mem.Allocator,
    io: Io,
    runtime_root: []const u8,
) ![]RuntimeForkPlanEntry {
    var entries = std.array_list.Managed(RuntimeForkPlanEntry).init(allocator);
    const refs = try collectRuntimeForkRefs(allocator, io, runtime_root);
    const forks_dir_path = try std.fs.path.join(allocator, &.{ runtime_root, "forks" });
    var forks_dir = openDirPath(io, forks_dir_path, .{ .iterate = true }) catch |err| switch (err) {
        error.FileNotFound => return entries.toOwnedSlice(),
        else => |e| return e,
    };
    defer forks_dir.close(io);

    var it = forks_dir.iterate();
    while (try it.next(io)) |entry| {
        if (entry.kind != .directory) continue;
        const path = try std.fs.path.join(allocator, &.{ forks_dir_path, entry.name });
        if (pathReferenced(path, refs)) continue;
        const stat = try forks_dir.statFile(io, entry.name, .{ .follow_symlinks = false });
        if (stat.kind != .directory) continue;
        const stats = blk: {
            var batch_dir = try forks_dir.openDir(io, entry.name, .{ .iterate = true, .follow_symlinks = false });
            defer batch_dir.close(io);
            break :blk try treeStatsDir(allocator, io, batch_dir, path);
        };
        try entries.append(.{
            .path = path,
            .bytes = stats.bytes,
            .mtime_ns = stat.mtime.nanoseconds,
        });
    }

    return entries.toOwnedSlice();
}

fn collectRuntimeForkRefs(allocator: std.mem.Allocator, io: Io, runtime_root: []const u8) ![]const []const u8 {
    var refs = std.array_list.Managed([]const u8).init(allocator);
    const vms_dir_path = try std.fs.path.join(allocator, &.{ runtime_root, "vms" });
    var vms_dir = openDirPath(io, vms_dir_path, .{ .iterate = true }) catch |err| switch (err) {
        error.FileNotFound => return refs.toOwnedSlice(),
        else => |e| return e,
    };
    defer vms_dir.close(io);

    var it = vms_dir.iterate();
    while (try it.next(io)) |entry| {
        if (entry.kind != .directory) continue;
        const spec_path = try std.fs.path.join(allocator, &.{ vms_dir_path, entry.name, "spec.json" });
        const data = Io.Dir.cwd().readFileAlloc(io, spec_path, allocator, .limited(max_runtime_spec_bytes)) catch |err| switch (err) {
            error.FileNotFound => continue,
            else => |e| return e,
        };
        defer allocator.free(data);
        var parsed = std.json.parseFromSlice(RuntimeVmSpecRef, allocator, data, .{
            .allocate = .alloc_always,
            .ignore_unknown_fields = true,
        }) catch continue;
        defer parsed.deinit();
        const resume_dir = parsed.value.resume_dir orelse continue;
        try refs.append(try std.fs.path.resolve(allocator, &.{resume_dir}));
    }

    return refs.toOwnedSlice();
}

fn pathReferenced(path: []const u8, refs: []const []const u8) bool {
    for (refs) |ref| {
        if (pathContains(path, ref)) return true;
    }
    return false;
}

fn pathContains(parent: []const u8, child: []const u8) bool {
    if (!std.mem.startsWith(u8, child, parent)) return false;
    if (child.len == parent.len) return true;
    if (parent.len > 0 and parent[parent.len - 1] == std.fs.path.sep) return true;
    return child[parent.len] == std.fs.path.sep;
}

fn collectPruneEntries(
    allocator: std.mem.Allocator,
    io: Io,
    cache_root: []const u8,
    include_digest_artifacts: bool,
    include_rootfs_chunks: bool,
) ![]PrunePlanEntry {
    var entries = std.array_list.Managed(PrunePlanEntry).init(allocator);

    var root = openDirPath(io, cache_root, .{ .iterate = true }) catch |err| switch (err) {
        error.FileNotFound => return entries.toOwnedSlice(),
        else => |e| return e,
    };
    defer root.close(io);

    var it = root.iterate();
    while (try it.next(io)) |entry| {
        if (entry.kind != .file or !std.mem.endsWith(u8, entry.name, ".ext4")) continue;
        const stat = try root.statFile(io, entry.name, .{ .follow_symlinks = false });
        if (stat.kind != .file) continue;
        if (stat.nlink > 1 and !include_digest_artifacts) continue;
        const path = try std.fs.path.join(allocator, &.{ cache_root, entry.name });
        const metadata_path = try metadataPathForExt4(allocator, cache_root, entry.name);
        const metadata_size = fileSizeNoSymlink(io, metadata_path) catch |err| switch (err) {
            error.FileNotFound => 0,
            else => |e| return e,
        };
        try entries.append(.{
            .kind = .image_rootfs,
            .path = path,
            .metadata_path = if (metadata_size == 0) null else metadata_path,
            .bytes = stat.size,
            .metadata_bytes = metadata_size,
            .link_count = @intCast(stat.nlink),
            .mtime_ns = stat.mtime.nanoseconds,
        });
    }

    if (include_digest_artifacts) {
        try collectDigestArtifacts(allocator, io, cache_root, &entries);
    }
    // Rootfs CAS index and chunk objects are derived data: `spore pack`
    // regenerates them from the flat artifact and resume serves the flat
    // artifact directly, so they can be reclaimed independently of the flat
    // digest artifacts that are the resume authority.
    if (include_rootfs_chunks) {
        try collectCasArtifacts(allocator, io, cache_root, "cas/rootfs/blake3/indexes", ".json", .cas_index, &entries);
        try collectCasArtifacts(allocator, io, cache_root, "cas/rootfs/blake3/objects", ".chunk", .cas_object, &entries);
    }

    return entries.toOwnedSlice();
}

fn collectCasArtifacts(
    allocator: std.mem.Allocator,
    io: Io,
    cache_root: []const u8,
    sub_path: []const u8,
    suffix: []const u8,
    kind: RootfsEntryKind,
    entries: *std.array_list.Managed(PrunePlanEntry),
) !void {
    const dir_path = try std.fs.path.join(allocator, &.{ cache_root, sub_path });
    var dir = openDirPath(io, dir_path, .{ .iterate = true }) catch |err| switch (err) {
        error.FileNotFound => return,
        else => |e| return e,
    };
    defer dir.close(io);

    var it = dir.iterate();
    while (try it.next(io)) |entry| {
        if (entry.kind != .file or !std.mem.endsWith(u8, entry.name, suffix)) continue;
        const stat = try dir.statFile(io, entry.name, .{ .follow_symlinks = false });
        if (stat.kind != .file) continue;
        const path = try std.fs.path.join(allocator, &.{ dir_path, entry.name });
        try entries.append(.{
            .kind = kind,
            .path = path,
            .bytes = stat.size,
            .link_count = @intCast(stat.nlink),
            .mtime_ns = stat.mtime.nanoseconds,
        });
    }
}

