fn normalizePruneOptions(options: PruneOptions) !PrunePlanOptions {
    var out = PrunePlanOptions{
        .dry_run = options.dry_run,
        .include_digest_artifacts = options.include_digest_artifacts,
        .include_rootfs_chunks = options.include_rootfs_chunks,
        .older_than_seconds = options.older_than_seconds,
        .max_bytes = options.max_bytes,
    };

    if (options.older_than_seconds) |seconds| {
        out.older_than_ns = std.math.mul(i96, @intCast(seconds), std.time.ns_per_s) catch return error.InvalidPruneSelection;
    }

    if (out.older_than_ns == null and out.max_bytes == null) {
        if (out.include_digest_artifacts or out.include_rootfs_chunks) return error.InvalidPruneSelection;
        out.max_bytes = default_prune_max_bytes;
        out.default_selection = true;
    }
    return out;
}

fn ownRootfsPruneResult(allocator: std.mem.Allocator, result: RootfsPruneResult) !RootfsPruneResult {
    var out = result;
    out.cache_root = try allocator.dupe(u8, result.cache_root);
    errdefer allocator.free(out.cache_root);

    const entries = try allocator.alloc(RootfsPruneEntry, result.entries.len);
    var owned_entry_count: usize = 0;
    errdefer {
        for (entries[0..owned_entry_count]) |entry| {
            allocator.free(entry.path);
            if (entry.metadata_path) |path| allocator.free(path);
        }
        allocator.free(entries);
    }
    for (entries, result.entries) |*dest, source| {
        dest.* = source;
        dest.path = "";
        dest.metadata_path = null;
        dest.path = try allocator.dupe(u8, source.path);
        owned_entry_count += 1;
        if (source.metadata_path) |path| {
            dest.metadata_path = try allocator.dupe(u8, path);
        }
    }
    out.entries = entries;

    var runtime_owned = false;
    errdefer if (runtime_owned) {
        if (out.runtime_forks) |owned_runtime_forks| {
            deinitRuntimeForkPruneResult(allocator, owned_runtime_forks);
        }
    };
    if (result.runtime_forks) |runtime_forks| {
        out.runtime_forks = try ownRuntimeForkPruneResult(allocator, runtime_forks);
        runtime_owned = true;
    }
    return out;
}

fn ownRootfsGcResult(allocator: std.mem.Allocator, result: RootfsGcResult) !RootfsGcResult {
    var out = result;
    out.cache_root = try allocator.dupe(u8, result.cache_root);
    errdefer allocator.free(out.cache_root);

    const entries = try allocator.alloc(RootfsGcEntry, result.entries.len);
    var owned_entry_count: usize = 0;
    errdefer {
        for (entries[0..owned_entry_count]) |entry| allocator.free(entry.path);
        allocator.free(entries);
    }
    for (entries, result.entries) |*dest, source| {
        dest.* = source;
        dest.path = "";
        dest.path = try allocator.dupe(u8, source.path);
        owned_entry_count += 1;
    }
    out.entries = entries;
    return out;
}

fn ownRuntimeForkPruneResult(allocator: std.mem.Allocator, result: RuntimeForkPruneResult) !RuntimeForkPruneResult {
    var out = result;
    out.runtime_root = try allocator.dupe(u8, result.runtime_root);
    errdefer allocator.free(out.runtime_root);

    const entries = try allocator.alloc(RuntimeForkPruneEntry, result.entries.len);
    var owned_entry_count: usize = 0;
    errdefer {
        for (entries[0..owned_entry_count]) |entry| allocator.free(entry.path);
        allocator.free(entries);
    }
    for (entries, result.entries) |*dest, source| {
        dest.* = source;
        dest.path = "";
        dest.path = try allocator.dupe(u8, source.path);
        owned_entry_count += 1;
    }
    out.entries = entries;
    return out;
}

fn deinitRuntimeForkPruneResult(allocator: std.mem.Allocator, result: RuntimeForkPruneResult) void {
    allocator.free(result.runtime_root);
    for (result.entries) |entry| allocator.free(entry.path);
    allocator.free(result.entries);
}

fn summarizeRootfsCache(allocator: std.mem.Allocator, io: Io, cache_root: []const u8) !RootfsSystemSummary {
    var summary = RootfsSystemSummary{ .cache_root = cache_root };
    var root = openDirPath(io, cache_root, .{ .iterate = true }) catch |err| switch (err) {
        error.FileNotFound => return summary,
        else => |e| return e,
    };
    defer root.close(io);

    var it = root.iterate();
    while (try it.next(io)) |entry| {
        if (entry.kind == .file and std.mem.endsWith(u8, entry.name, ".ext4")) {
            const stat = try root.statFile(io, entry.name, .{ .follow_symlinks = false });
            summary.image_rootfs.count += 1;
            summary.image_rootfs.bytes += stat.size;
            if (stat.nlink > 1) {
                summary.linked_image_rootfs.count += 1;
                summary.linked_image_rootfs.bytes += stat.size;
            } else {
                const metadata_path = try metadataPathForExt4(allocator, cache_root, entry.name);
                const metadata_size = fileSizeNoSymlink(io, metadata_path) catch |err| switch (err) {
                    error.FileNotFound => 0,
                    else => |e| return e,
                };
                summary.default_prunable_bytes += stat.size + metadata_size;
            }
        } else if (entry.kind == .file and std.mem.endsWith(u8, entry.name, ".json")) {
            const stat = try root.statFile(io, entry.name, .{ .follow_symlinks = false });
            summary.image_metadata.count += 1;
            summary.image_metadata.bytes += stat.size;
        } else if (entry.kind == .directory and std.mem.eql(u8, entry.name, "refs")) {
            summary.ref_records = try treeStats(allocator, io, cache_root, "refs");
        } else if (entry.kind == .directory and std.mem.eql(u8, entry.name, "tmp")) {
            summary.temp_entries = try treeStats(allocator, io, cache_root, "tmp");
        }
    }

    summary.digest_artifacts = try digestArtifactStats(allocator, io, cache_root);
    summary.cas_indexes = try treeStats(allocator, io, cache_root, "cas/rootfs/blake3/indexes");
    summary.cas_objects = try treeStats(allocator, io, cache_root, "cas/rootfs/blake3/objects");
    summary.cas_complete_stamps = try treeStats(allocator, io, cache_root, "cas/rootfs/blake3/complete");
    summary.known_logical_bytes = summary.image_rootfs.bytes + summary.image_metadata.bytes +
        summary.digest_artifacts.bytes + summary.cas_indexes.bytes + summary.cas_objects.bytes +
        summary.cas_complete_stamps.bytes + summary.ref_records.bytes + summary.temp_entries.bytes;
    return summary;
}

fn pruneRootfsCache(
    allocator: std.mem.Allocator,
    io: Io,
    cache_root: []const u8,
    opts: PrunePlanOptions,
    now_ns: i96,
) !RootfsPruneResult {
    // Planning and deletion must observe one stable cache generation. This is
    // the same lock held by rootfs publishers, so a forced prune cannot select
    // objects that become part of newly completed storage before deletion.
    var cache_lock = try rootfs.lockRootfsCacheExclusive(io, allocator, cache_root);
    defer cache_lock.deinit();

    const plan = try collectPruneEntries(allocator, io, cache_root, opts.include_digest_artifacts, opts.include_rootfs_chunks);
    std.mem.sort(PrunePlanEntry, plan, {}, lessPrunePlanEntry);

    var scope_bytes_before: u64 = 0;
    for (plan) |entry| scope_bytes_before += entry.logicalBytes();

    var remaining = scope_bytes_before;
    if (opts.older_than_ns) |age_ns| {
        const cutoff = now_ns - age_ns;
        for (plan) |*entry| {
            if (entry.mtime_ns < cutoff) {
                entry.selected = true;
                remaining -= entry.logicalBytes();
            }
        }
    }

    if (opts.max_bytes) |max_bytes| {
        for (plan) |*entry| {
            if (remaining <= max_bytes) break;
            if (entry.selected) continue;
            entry.selected = true;
            remaining -= entry.logicalBytes();
        }
    }

    var result_entries = std.array_list.Managed(RootfsPruneEntry).init(allocator);
    var candidate_bytes: u64 = 0;
    var candidate_reclaimable_bytes: u64 = 0;
    var deleted_count: usize = 0;
    var deleted_bytes: u64 = 0;
    var deleted_reclaimable_bytes: u64 = 0;

    var selected_rootfs_object_digests = std.StringHashMap(void).init(allocator);
    defer deinitRootfsObjectDigestSet(allocator, &selected_rootfs_object_digests);
    if (!opts.dry_run) {
        try collectSelectedRootfsObjectDigests(allocator, plan, &selected_rootfs_object_digests);
        try rootfs_cas.removeStorageCompleteStampsReferencingObjects(io, allocator, cache_root, selected_rootfs_object_digests);
    }

    for (plan) |entry| {
        if (!entry.selected) continue;
        const logical = entry.logicalBytes();
        const reclaimable = entry.reclaimableBytes();
        candidate_bytes += logical;
        candidate_reclaimable_bytes += reclaimable;
        try result_entries.append(.{
            .kind = entry.kind.label(),
            .path = entry.path,
            .metadata_path = entry.metadata_path,
            .bytes = entry.bytes,
            .metadata_bytes = entry.metadata_bytes,
            .reclaimable_bytes = reclaimable,
            .link_count = entry.link_count,
            .mtime_unix = @intCast(@divFloor(entry.mtime_ns, std.time.ns_per_s)),
        });
        if (!opts.dry_run) {
            try invalidateRootfsCompleteStampForPruneEntry(allocator, io, cache_root, entry);
            try Io.Dir.cwd().deleteFile(io, entry.path);
            if (entry.metadata_path) |metadata_path| {
                Io.Dir.cwd().deleteFile(io, metadata_path) catch |err| switch (err) {
                    error.FileNotFound => {},
                    else => |e| return e,
                };
            }
            deleted_count += 1;
            deleted_bytes += logical;
            deleted_reclaimable_bytes += reclaimable;
        }
    }

    const entries = try result_entries.toOwnedSlice();
    return .{
        .cache_root = cache_root,
        .dry_run = opts.dry_run,
        .include_digest_artifacts = opts.include_digest_artifacts,
        .include_rootfs_chunks = opts.include_rootfs_chunks,
        .older_than_seconds = opts.older_than_seconds,
        .max_bytes = opts.max_bytes,
        .scope_bytes_before = scope_bytes_before,
        .scope_bytes_after = scope_bytes_before - candidate_bytes,
        .candidate_count = entries.len,
        .candidate_bytes = candidate_bytes,
        .candidate_reclaimable_bytes = candidate_reclaimable_bytes,
        .default_selection = opts.default_selection,
        .deleted_count = deleted_count,
        .deleted_bytes = deleted_bytes,
        .deleted_reclaimable_bytes = deleted_reclaimable_bytes,
        .entries = entries,
    };
}

fn invalidateRootfsCompleteStampForPruneEntry(
    allocator: std.mem.Allocator,
    io: Io,
    cache_root: []const u8,
    entry: PrunePlanEntry,
) !void {
    switch (entry.kind) {
        .cas_index => {
            const name = std.fs.path.basename(entry.path);
            const digest = digestFromCacheEntryName(allocator, name, ".json") orelse return;
            defer allocator.free(digest);
            try rootfs_cas.removeStorageCompleteStamp(io, allocator, cache_root, digest);
        },
        else => {},
    }
}

fn collectSelectedRootfsObjectDigests(
    allocator: std.mem.Allocator,
    plan: []const PrunePlanEntry,
    digests: *std.StringHashMap(void),
) !void {
    for (plan) |entry| {
        if (!entry.selected or entry.kind != .cas_object) continue;
        const name = std.fs.path.basename(entry.path);
        const digest = digestFromCacheEntryName(allocator, name, ".chunk") orelse continue;
        const result = digests.getOrPut(digest) catch |err| {
            allocator.free(digest);
            return err;
        };
        if (result.found_existing) allocator.free(digest);
    }
}

fn deinitRootfsObjectDigestSet(allocator: std.mem.Allocator, digests: *std.StringHashMap(void)) void {
    var it = digests.iterator();
    while (it.next()) |entry| allocator.free(entry.key_ptr.*);
    digests.deinit();
}

fn gcRootfsCache(
    allocator: std.mem.Allocator,
    io: Io,
    options: GcOptions,
) !RootfsGcResult {
    var cache_lock = try rootfs.lockRootfsCacheExclusive(io, allocator, options.cache_root);
    defer cache_lock.deinit();

    var storage_roots = std.StringHashMap(spore.RootfsStorage).init(allocator);
    var rooted_indexes = std.StringHashMap(void).init(allocator);
    var rooted_objects = std.StringHashMap(void).init(allocator);
    var conservative_roots = GcConservativeRootStats{};

    try collectRootfsStorageRoots(allocator, io, options.cache_root, options.runtime_root, &storage_roots, &conservative_roots);
    if (conservative_roots.any()) {
        try markAllGcCasEntriesRooted(allocator, io, options.cache_root, "cas/rootfs/blake3/indexes", ".json", &rooted_indexes);
        try markAllGcCasEntriesRooted(allocator, io, options.cache_root, "cas/rootfs/blake3/objects", ".chunk", &rooted_objects);
    } else {
        var storage_it = storage_roots.iterator();
        while (storage_it.next()) |entry| {
            const storage = entry.value_ptr.*;
            try rooted_indexes.put(storage.index_digest, {});
            const index_path = try rootfs_cas.manifestIndexPath(allocator, options.cache_root, storage.index_digest);
            const bytes = Io.Dir.cwd().readFileAlloc(io, index_path, allocator, .limited(disk_index.max_index_bytes)) catch |err| switch (err) {
                error.FileNotFound, error.StreamTooLong => return error.BadManifest,
                else => |e| return e,
            };
            const descriptor = spore.diskIndexDescriptorForStorage(storage) catch return error.BadManifest;
            const parsed = disk_index.parseDiskIndex(allocator, bytes, descriptor) catch return error.BadManifest;
            for (parsed.value.chunks) |chunk_entry| {
                try rooted_objects.put(chunk_entry.digest, {});
            }
        }
    }

    var result_entries = std.array_list.Managed(RootfsGcEntry).init(allocator);
    var candidate_bytes: u64 = 0;
    var deleted_count: usize = 0;
    var deleted_bytes: u64 = 0;

    try collectGcCasEntries(
        allocator,
        io,
        options.cache_root,
        "cas/rootfs/blake3/indexes",
        ".json",
        "rootfs-cas-index",
        &rooted_indexes,
        .index,
        options.dry_run,
        &result_entries,
        &candidate_bytes,
        &deleted_count,
        &deleted_bytes,
    );
    try collectGcCasEntries(
        allocator,
        io,
        options.cache_root,
        "cas/rootfs/blake3/objects",
        ".chunk",
        "rootfs-cas-object",
        &rooted_objects,
        .none,
        options.dry_run,
        &result_entries,
        &candidate_bytes,
        &deleted_count,
        &deleted_bytes,
    );

    const entries = try result_entries.toOwnedSlice();
    return .{
        .cache_root = options.cache_root,
        .dry_run = options.dry_run,
        .rooted_indexes = rooted_indexes.count(),
        .rooted_objects = rooted_objects.count(),
        .unparseable_metadata_count = conservative_roots.metadata_count,
        .unparseable_ref_count = conservative_roots.ref_count,
        .unparseable_step_record_count = conservative_roots.step_record_count,
        .candidate_count = entries.len,
        .candidate_bytes = candidate_bytes,
        .deleted_count = deleted_count,
        .deleted_bytes = deleted_bytes,
        .entries = entries,
    };
}

